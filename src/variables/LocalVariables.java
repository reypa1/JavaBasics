package variables;

public class LocalVariables {

	public static void main(String[] args) {
	
    /*Local variable : when a variable is created inside the method or a block of code it is known as local variable.
     * Whenever local variable is created it will be stored in stack memory. 
     * Local variable can't be called from out side the method.
     */
		int age=26;
		double salary=45000;
		System.out.println("My age is  : "+age);
		System.out.println("My salary is  : "+salary);
	}
}
